#include <stdio.h>

int	ft_isalnum(int x)
{
	if ((x >= '0' && x <= '9')
		|| (x >= 'a' && x <= 'z')
		|| (x >= 'A' && x <= 'Z'))
	{
		return (1);
	}
	else
	{
		return (0);
	}
}

